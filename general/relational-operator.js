/*
    RELATIONAL OPERATORS (comparison operators)

    ==
    === => IDENTICAL, variables must be the same type

    !=
    !==

    >   => Bigger than
    <   => Small than
    >=  => Bigger or Equal than
    <=  => Less or Equal than

*/

var a = 5;
var b = '5';

console.log(a == b);
console.log(a === b);

console.log(a != b);
console.log(a !== b);
